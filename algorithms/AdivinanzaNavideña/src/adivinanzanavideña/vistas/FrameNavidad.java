    private void ejecucion(String emoji) {
        try {
            int cantidad = Integer.parseInt(jtfCantidad.getText());

            if (cantidad >= 0 && cantidad <= 16) {
                matriz = control.generarMatriz();
                cantEncontrados = control.determinarCantidadEmoji(emoji, cantidad, matriz);

                jtfEncontrados.setText(String.valueOf(cantEncontrados));
                jtfElegido.setText(emoji);
                jtfCant.setText(String.valueOf(cantidad));

                if (cantEncontrados == cantidad) {
                    jtfResultado.setText("ACERTO!");
                } else {
                    jtfResultado.setText("FALLO!");
                }
                dibujarMatriz(matriz);
            } else {
                JOptionPane.showMessageDialog(null, "ingrese un numero del 1 al 16");
                limpiarCampos();
            }
        } catch (NullPointerException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ingrese un numero valido");
        }
    }

    private void limpiarCampos() {
        jtfCant.setText("");
        jtfCantidad.setText("");
        jtfElegido.setText("");
        jtfEncontrados.setText("");
        jtfResultado.setText("");
        jTextArea.setText("");

    }

    private void dibujarMatriz(String matriz[][]) {
        for (int f = 0; f < 4; f++) {
            for (int c = 0; c < 4; c++) {

                jTextArea.setText(jTextArea.getText() + matriz[f][c] + " ");

            }
            jTextArea.setText(jTextArea.getText() + "\n");
        }
    }
